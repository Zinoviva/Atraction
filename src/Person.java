public class Person {  //условие у каждого человека имя фамилия и кол.билетов
    public String name;  //имя
    public String lastName;  //фамилия
    public int ticket;  //билет

    public Person(String name, String lastName, int ticket) {
        this.name = name;
        this.lastName = lastName;
        this.ticket = ticket;
    }
}
